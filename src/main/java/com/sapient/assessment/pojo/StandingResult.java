package com.sapient.assessment.pojo;

public class StandingResult {
	private String countryId;
	private String countryNm;

	private String leagueId;
	private String leagueNm;

	private String teamId;
	private String teamNm;

	private String standingPosition;

	/**
	 * @return the countryId
	 */
	public String getCountryId() {
		return countryId;
	}

	/**
	 * @param countryId the countryId to set
	 */
	public void setCountryId(String countryId) {
		this.countryId = countryId;
	}

	/**
	 * @return the countryNm
	 */
	public String getCountryNm() {
		return countryNm;
	}

	/**
	 * @param countryNm the countryNm to set
	 */
	public void setCountryNm(String countryNm) {
		this.countryNm = countryNm;
	}

	/**
	 * @return the leagueId
	 */
	public String getLeagueId() {
		return leagueId;
	}

	/**
	 * @param leagueId the leagueId to set
	 */
	public void setLeagueId(String leagueId) {
		this.leagueId = leagueId;
	}

	/**
	 * @return the leagueNm
	 */
	public String getLeagueNm() {
		return leagueNm;
	}

	/**
	 * @param leagueNm the leagueNm to set
	 */
	public void setLeagueNm(String leagueNm) {
		this.leagueNm = leagueNm;
	}

	/**
	 * @return the teamId
	 */
	public String getTeamId() {
		return teamId;
	}

	/**
	 * @param teamId the teamId to set
	 */
	public void setTeamId(String teamId) {
		this.teamId = teamId;
	}

	/**
	 * @return the teamNm
	 */
	public String getTeamNm() {
		return teamNm;
	}

	/**
	 * @param teamNm the teamNm to set
	 */
	public void setTeamNm(String teamNm) {
		this.teamNm = teamNm;
	}

	/**
	 * @return the standingPosition
	 */
	public String getStandingPosition() {
		return standingPosition;
	}

	/**
	 * @param standingPosition the standingPosition to set
	 */
	public void setStandingPosition(String standingPosition) {
		this.standingPosition = standingPosition;
	}

}
